package review4;

public class nestedLoops2 {
    public static void main(String[] args) {

        //car odometer

        for (int i = 0; i <= 9; i++) {
            for (int j = 0; j <= 9; j++) {
                for (int k = 0; k <= 9; k++) {
                    System.out.println(i + " " + j + " " + k);
                }
            }
        }


    }
}
