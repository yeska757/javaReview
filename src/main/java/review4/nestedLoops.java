package review4;

public class nestedLoops {
    public static void main(String[] args) {

        for (int i = 1; i <=3 ; i++) {    //outer loop

            System.out.println(i);

            for (int j = 0; j <=1; j++) {    //inner loop

                System.out.println(j);
            }
        }











    }
}
